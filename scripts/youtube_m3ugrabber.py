def grab(url):
    response = requests.get(url, timeout=15).text
    if '.m3u8' not in response:
        raise TypeError
    else:
        a=response.split('"')
        for line in a:
            if line.startswith("https://manifest.googlevideo.com/api/manifest/hls_variant/expire") and line.endswith("index.m3u8"):
                print(line)

